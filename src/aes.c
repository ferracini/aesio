/* Application of the AES S-box to each of the four bytes of the word. */
static inline uint32_t SubWord(
  uint32_t value)
{
  return
    (sbox[((value >> 28) & 0xf) * (SBOX_DIM) + ((value >> 24) & 0xf)] << 24)  |
    (sbox[((value >> 20) & 0xf) * (SBOX_DIM) + ((value >> 16) & 0xf)] << 16)  |
    (sbox[((value >> 12) & 0xf) * (SBOX_DIM) + ((value >> 8) & 0xf)] << 8)    |
    (sbox[((value >> 4) & 0xf)  * (SBOX_DIM) + ((value >> 0) & 0xf)] << 0);
}

/* Key schedule function one-byte left circular shift. */
static inline uint32_t RotWord(
  uint32_t value,
  int rotCount)
{
  return _ROTL(value, rotCount * CHAR_BIT);
}

void KeySchedule128(
  uint32_t* subKeys, 
  uint32_t* cKey)
{
  uint32_t temp = cKey[3];
  int i = AES_128_NK;

  subKeys[0] = cKey[0];
  subKeys[1] = cKey[1];
  subKeys[2] = cKey[2];
  subKeys[3] = cKey[3];
  while (i < AES_128_SUBKEYS_COUNT)
  {
    temp = RotWord(temp, 1);
    temp = SubWord(temp);
    temp ^= rcon[i / AES_128_NK];
    subKeys[i] = temp ^ subKeys[i - AES_128_NK];
    temp = subKeys[i++];
    subKeys[i] = temp ^ subKeys[i - AES_128_NK];
    temp = subKeys[i++];
    subKeys[i] = temp ^ subKeys[i - AES_128_NK];
    temp = subKeys[i++];
    subKeys[i] = temp ^ subKeys[i - AES_128_NK];
    temp = subKeys[i++];
  }
  temp = 0;
}

