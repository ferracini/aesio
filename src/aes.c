/* Transpose a 4x4 8-bit matrix */
static inline void TransposeMatrix(
  uint32_t* state,
  const uint32_t* src)
{
  state[0] = (src[0] & 0xFF000000) | ((src[1] & 0xFF000000) >> 8) | ((src[2] & 0xFF000000) >> 16) | ((src[3] & 0xFF000000) >> 24);
  state[1] = ((src[0] & 0x00FF0000) << 8) | ((src[1] & 0xFF0000)) | ((src[2] & 0xFF0000) >> 8) | ((src[3] & 0xFF0000) >> 16);
  state[2] = ((src[0] & 0xFF00) << 16) | ((src[1] & 0xFF00) << 8) | ((src[2] & 0xFF00)) | ((src[3] & 0xFF00) >> 8);
  state[3] = ((src[0] & 0xFF) << 24) | ((src[1] & 0xFF) << 16) | ((src[2] & 0xFF) << 8) | ((src[3] & 0xFF));
}

/* AES step: each byte of the state is combined with a block of the round key using bitwise xor. */
static inline void AddRoundKey(
  uint32_t* state,
  uint32_t* subKey)
{
  for (int i = 0; i < 4; i++)
  {
    state[i] ^= subKey[i];
  }    
}

/* AES step: cyclically shifts the bytes in each row.*/
static inline void ShiftRows(
  uint32_t* state)
{
  state[1] = RotWord(state[1], 1);
  state[2] = RotWord(state[2], 2);
  state[3] = RotWord(state[3], 3);
}

/* AES step: inverse ShiftRows */
static inline void InvShiftRows(
  uint32_t* state)
{
  state[1] = RotWord(state[1], 3);
  state[2] = RotWord(state[2], 2);
  state[3] = RotWord(state[3], 1);
}

/* AES step: non-linear substitution step where each byte is replaced with another according to a SBOX lookup table. */
static inline void SubBytes(
  uint32_t* state)
{
  for (int i = 0; i < 4; i++)
  {
    uint32_t y1 = (state[i] & 0xF0000000) >> 28;
    uint32_t x1 = (state[i] & 0x0F000000) >> 24;
    uint32_t y2 = (state[i] & 0x00F00000) >> 20;
    uint32_t x2 = (state[i] & 0x000F0000) >> 16;
    uint32_t y3 = (state[i] & 0x0000F000) >> 12;
    uint32_t x3 = (state[i] & 0x00000F00) >> 8;
    uint32_t y4 = (state[i] & 0x000000F0) >> 4;
    uint32_t x4 = (state[i] & 0x0000000F);
    state[i] =
      (sbox[y1 * (SBOX_DIM) + x1] << 24)  |
      (sbox[y2 * (SBOX_DIM) + x2] << 16)  | 
      (sbox[y3 * (SBOX_DIM) + x3] << 8)  | 
      (sbox[y4 * (SBOX_DIM) + x4]);    
  }
}

/* AES step: inverse SubBytes. */
static inline void InvSubBytes(
  uint32_t* state)
{
  for (int i = 0; i < 4; i++)
  {
    uint32_t y1 = (state[i] & 0xF0000000) >> 28;
    uint32_t x1 = (state[i] & 0x0F000000) >> 24;
    uint32_t y2 = (state[i] & 0x00F00000) >> 20;
    uint32_t x2 = (state[i] & 0x000F0000) >> 16;
    uint32_t y3 = (state[i] & 0x0000F000) >> 12;
    uint32_t x3 = (state[i] & 0x00000F00) >> 8;
    uint32_t y4 = (state[i] & 0x000000F0) >> 4;
    uint32_t x4 = (state[i] & 0x0000000F);
    state[i] =
      (invSbox[y1 * (SBOX_DIM) + x1] << 24) |
      (invSbox[y2 * (SBOX_DIM) + x2] << 16) |
      (invSbox[y3 * (SBOX_DIM) + x3] << 8) |
      (invSbox[y4 * (SBOX_DIM) + x4]);
  }
}

/* AES step: performs a matrix multiplication of a given state with a static matrix. */
static inline void MixColumns(
  uint32_t* state)
{
  uint8_t* pBlock0 = (uint8_t*)state;
  uint8_t* pBlock1 = (uint8_t*)&state[1];
  uint8_t* pBlock2 = (uint8_t*)&state[2];
  uint8_t* pBlock3 = (uint8_t*)&state[3];

  for (int i = 0; i < 4; i++)
  {
    uint8_t a0 = pBlock0[i];
    uint8_t a1 = pBlock1[i];
    uint8_t a2 = pBlock2[i];
    uint8_t a3 = pBlock3[i];
    pBlock0[i] = mult2[a0] ^ mult3[a1] ^ a2 ^ a3;
    pBlock1[i] = a0 ^ mult2[a1] ^ mult3[a2] ^ a3;
    pBlock2[i] = a0 ^ a1 ^ mult2[a2] ^ mult3[a3];
    pBlock3[i] = mult3[a0] ^ a1 ^ a2 ^ mult2[a3];
  }
}

/* AES step: inverse MixColumns. */
static inline void InvMixColumns(
  uint32_t* state)
{
  uint8_t* pBlock0 = (uint8_t*)state;
  uint8_t* pBlock1 = (uint8_t*)&state[1];
  uint8_t* pBlock2 = (uint8_t*)&state[2];
  uint8_t* pBlock3 = (uint8_t*)&state[3];
  for (int i = 0; i < 4; ++i)
  {    
    const uint8_t a = pBlock0[i];
    const uint8_t b = pBlock1[i];
    const uint8_t c = pBlock2[i];
    const uint8_t d = pBlock3[i];    
    pBlock0[i] = mult14[a] ^ mult11[b] ^ mult13[c] ^ mult9[d];
    pBlock1[i] = mult9[a] ^ mult14[b] ^ mult11[c] ^ mult13[d];
    pBlock2[i] = mult13[a] ^ mult9[b] ^ mult14[c] ^ mult11[d];
    pBlock3[i] = mult11[a] ^ mult13[b] ^ mult9[c] ^ mult14[d];
  }
}

void AesEncryptBlock(
  uint32_t* state, 
  uint32_t* subKeys, 
  const size_t subKeysCount,
  const size_t nRounds)
{
  uint32_t subKeysBlock[4];
  uint32_t blockBuffer[4];

  TransposeMatrix(blockBuffer, state);
  TransposeMatrix(subKeysBlock, subKeys);
  AddRoundKey(blockBuffer, subKeysBlock);

  for (size_t i = 0; i < nRounds; i++)
  {
    SubBytes(blockBuffer);
    ShiftRows(blockBuffer);
    MixColumns(blockBuffer);
    TransposeMatrix(subKeysBlock, &subKeys[4 + (i << 2)]);
    AddRoundKey(blockBuffer, subKeysBlock);
  }

  SubBytes(blockBuffer);
  ShiftRows(blockBuffer);
  TransposeMatrix(subKeysBlock, &subKeys[subKeysCount - 4]);
  AddRoundKey(blockBuffer, subKeysBlock);
  TransposeMatrix(state, blockBuffer);
}

void AesDecryptBlock(
  uint32_t* state, 
  uint32_t* subKeys,
  const size_t subKeysCount,
  const size_t nRounds)
{
  uint32_t subKeysBlock[4];
  uint32_t blockBuffer[4];

  TransposeMatrix(blockBuffer, state);
  TransposeMatrix(subKeysBlock, &subKeys[subKeysCount - 4]);
  AddRoundKey(blockBuffer, subKeysBlock);
  InvSubBytes(blockBuffer);
  InvShiftRows(blockBuffer);

  for (size_t i = 0; i < nRounds; i++)
  {
    TransposeMatrix(subKeysBlock, &subKeys[subKeysCount - 8 - (i << 2)]);
    AddRoundKey(blockBuffer, subKeysBlock);
    InvMixColumns(blockBuffer);
    InvSubBytes(blockBuffer);
    InvShiftRows(blockBuffer);
  }

  TransposeMatrix(subKeysBlock, subKeys);
  AddRoundKey(blockBuffer, subKeysBlock);
  TransposeMatrix(state, blockBuffer);
}
